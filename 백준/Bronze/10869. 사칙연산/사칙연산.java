import java.util.Scanner;

public class Main{
    public static void main(String[] args){
    	Scanner sc = new Scanner(System.in);
    	
    	// a 입력받기
    	int a = sc.nextInt();
    	// b 입력받기
    	int b = sc.nextInt();
    	
    	// a+b 출력
    	System.out.println(a+b);
    	
    	// a-b 출력
    	System.out.println(a-b);
    	
    	// a*b 출력
    	System.out.println(a*b);
    	
    	// a/b 출력
    	System.out.println(a/b);
    	
    	// a%b 출력
    	System.out.println(a%b);
    }
}